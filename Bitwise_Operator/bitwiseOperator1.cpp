#include <bits/stdc++.h>
using namespace std;


int main() {
    int a = 5;
    int b = 7;

    cout << "a&b : " << (a&b) << endl;
    cout << "a|b : " << (a|b) << endl;
    cout << "~a : " << (~a) << endl;
    cout << "a^b : " << (a^b) << endl;



    // left and right shift
    cout << "a<<1 : " << (a<<1) << endl;
    cout << "a>>1 : " << (a>>1) << endl;
    cout << "17>>1 : " << (17>>1) << endl;
    cout << "17>>2 : " << (17>>2) << endl;
    cout << "19<<1 : " << (19<<1) << endl;
    cout << "21<<2 : " << (21<<2) << endl;
}